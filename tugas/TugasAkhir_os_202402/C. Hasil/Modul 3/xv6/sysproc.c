int sys_shmget(void)
{
  int key;
  if (argint(0, &key) < 0)
    return -1;
  return (int)shmget(key);
}

int sys_shmrelease(void) 
{
  int key;
  if (argint(0, &key) < 0)
    return -1;
  shmrelease(key);
  return 0;
}

